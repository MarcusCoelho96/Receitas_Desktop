    protected void tamanhoColunas() {

        tabelaReceitas.setAutoResizeMode(tabelaReceitas.AUTO_RESIZE_OFF);
        //alerar o tamanho das colunas
        tabelaReceitas.getColumnModel().getColumn(0).setPreferredWidth(35);
        tabelaReceitas.getColumnModel().getColumn(1).setPreferredWidth(200);
        tabelaReceitas.getColumnModel().getColumn(2).setPreferredWidth(315);
        tabelaReceitas.getColumnModel().getColumn(3).setPreferredWidth(100);

    }
    
    protected void limparCampos(){
        
        fieldIdReceitas.setText("");
        fieldNomeVisualizar.setText("");
        textAreaDetalhesVisualizar.setText("");
        fieldDataReceitas.setText("");
        
    }

    protected void listar() {

        try {
            ReceitaDAO receitaDAO = new ReceitaDAO();

            DefaultTableModel model = (DefaultTableModel) tabelaReceitas.getModel();

            model.setNumRows(0);

            ArrayList<Receita> lista = receitaDAO.listar();

            for (int i = 0; i < lista.size(); i++) {
                model.addRow(new Object[]{
                    lista.get(i).getIdReceita(),
                    lista.get(i).getNome(),
                    lista.get(i).getDetalhe(),
                    lista.get(i).getData()
                });
            }

        } catch (Exception e) {
        }
    }

    protected void visualizar() {

        //saber qual a linha da tabela que esta sendo selecionada
        int linha = tabelaReceitas.getSelectedRow();

        fieldIdReceitas.setText(tabelaReceitas.getModel().getValueAt(linha, 0).toString());
        fieldNomeVisualizar.setText(tabelaReceitas.getModel().getValueAt(linha, 1).toString());
        textAreaDetalhesVisualizar.setText(tabelaReceitas.getModel().getValueAt(linha, 2).toString());
        fieldDataReceitas.setText(tabelaReceitas.getModel().getValueAt(linha, 3).toString());

        fieldIdReceitas.setEditable(false);
        fieldNomeVisualizar.setEditable(false);
        textAreaDetalhesVisualizar.setEditable(false);
        fieldDataReceitas.setEditable(false);

    }

    protected void enviarCamposParaEditar() {

        int linha = tabelaReceitas.getSelectedRow();

        fieldIdReceitas.setEditable(false);
        fieldNomeVisualizar.setEditable(true);
        textAreaDetalhesVisualizar.setEditable(true);
        fieldDataReceitas.setEditable(true);

        fieldIdReceitas.setText(tabelaReceitas.getModel().getValueAt(linha, 0).toString());
        fieldNomeVisualizar.setText(tabelaReceitas.getModel().getValueAt(linha, 1).toString());
        textAreaDetalhesVisualizar.setText(tabelaReceitas.getModel().getValueAt(linha, 2).toString());
        fieldDataReceitas.setText(tabelaReceitas.getModel().getValueAt(linha, 3).toString());

    }

    private void alterarFuncionario() {

        int id;
        String nome;
        String detalhe;
        String data;

        id = Integer.parseInt(fieldIdReceitas.getText());
        nome = fieldNomeVisualizar.getText();
        detalhe = textAreaDetalhesVisualizar.getText();
        data = fieldDataReceitas.getText();

        Receita receita = new Receita();
        receita.setIdReceita(id);
        receita.setNome(nome);
        receita.setDetalhe(detalhe);
        receita.setData(data);

        ReceitaDAO receitaDAO = new ReceitaDAO();
        receitaDAO.alterar(receita);

    }
    
    private void deletar(){
        
        int id;
        
        id = Integer.parseInt(fieldIdReceitas.getText());
        
        Receita receita = new Receita();
        receita.setIdReceita(id);
        
        ReceitaDAO receitaDAO = new ReceitaDAO();
        receitaDAO.deletar(receita);
    }
